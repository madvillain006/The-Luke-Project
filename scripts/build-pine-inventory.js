#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');
const { execFileSync } = require('child_process');

const ROOT = path.join(__dirname, '..');
const PINE_DIR = path.join(ROOT, 'tradingview');
const OUT_MD = path.join(ROOT, 'docs', 'PINE_INVENTORY_GENERATED.md');
const OUT_JSON = path.join(ROOT, 'artifacts', 'proof', 'pine-inventory', 'pine-inventory.json');

function listTrackedPine() {
  try {
    return new Set(execFileSync('git', ['ls-files', 'tradingview'], {
      cwd: ROOT,
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore'],
    }).split(/\r?\n/).filter(file => file.endsWith('.pine')).map(file => file.replace(/\\/g, '/')));
  } catch {
    return new Set();
  }
}

function classifyFamily(name) {
  const lower = name.toLowerCase();
  if (lower.includes('production-test')) return 'production-test-ledger';
  if (lower.includes('level-reclaim')) return 'level-reclaim-base';
  if (lower.includes('flagship')) return 'flagship-candidate';
  if (lower.includes('saty')) return 'supporting-source';
  if (lower.includes('options')) return 'options-context';
  return 'other';
}

function analyzeFile(file, tracked) {
  const full = path.join(PINE_DIR, file);
  const text = fs.readFileSync(full, 'utf8');
  const rel = `tradingview/${file}`;
  const mode = /\bstrategy\s*\(/.test(text) || /\.strategy\.pine$/i.test(file)
    ? 'strategy'
    : /\bindicator\s*\(/.test(text) ? 'indicator' : 'unknown';
  const strategyCalls = (text.match(/\bstrategy\.(?:entry|order|exit|close|close_all)\s*\(/g) || []).length;
  const alertConditions = (text.match(/\balertcondition\s*\(/g) || []).length;
  const hasWebhookText = /\bwebhook|alert_message|ninja|tradovate|bridge\b/i.test(text);
  const likelyExecutionRisk = mode === 'strategy' || strategyCalls > 0 || /live.execution|exec.only|dryfire|broker/i.test(file);
  return {
    file: rel,
    bytes: fs.statSync(full).size,
    family: classifyFamily(file),
    mode,
    alert_conditions: alertConditions,
    strategy_calls: strategyCalls,
    bridge_terms: hasWebhookText,
    tracked: tracked.has(rel),
    promotion_status: 'not_promoted',
    allowed_use: 'visual/watchlist/research only until compile and signoff',
    risk: likelyExecutionRisk ? 'needs_explicit_gate_review' : 'standard_visual_review',
  };
}

function renderMarkdown(inventory) {
  const generatedAt = new Date().toISOString();
  const counts = inventory.reduce((acc, row) => {
    acc.total += 1;
    acc[row.family] = (acc[row.family] || 0) + 1;
    if (!row.tracked) acc.untracked += 1;
    if (row.risk === 'needs_explicit_gate_review') acc.gated += 1;
    return acc;
  }, { total: 0, untracked: 0, gated: 0 });

  const lines = [
    '# Pine Inventory Generated',
    '',
    `Generated: ${generatedAt}`,
    '',
    'This file is generated by `npm run tradingview:inventory`. It is not TradingView compile proof.',
    '',
    '## Summary',
    '',
    `- Total Pine files: ${counts.total}`,
    `- Untracked Pine files seen locally: ${counts.untracked}`,
    `- Files requiring explicit gate review: ${counts.gated}`,
    '',
    '## Family Counts',
    '',
    '| Family | Count |',
    '| --- | ---: |',
  ];

  for (const [family, count] of Object.entries(counts).filter(([key]) => !['total', 'untracked', 'gated'].includes(key)).sort()) {
    lines.push(`| ${family} | ${count} |`);
  }

  lines.push(
    '',
    '## Files',
    '',
    '| File | Family | Mode | Alerts | Strategy Calls | Tracked | Risk |',
    '| --- | --- | --- | ---: | ---: | --- | --- |',
  );

  for (const row of inventory) {
    lines.push(`| \`${row.file}\` | ${row.family} | ${row.mode} | ${row.alert_conditions} | ${row.strategy_calls} | ${row.tracked ? 'yes' : 'no'} | ${row.risk} |`);
  }

  lines.push(
    '',
    '## Gate',
    '',
    'No file in this generated inventory is promoted by this script. Promotion still requires TradingView compile, visual signoff, alert verification, and a written update to `docs/PINE_INVENTORY_AND_FLAGSHIP_GATE.md`.',
    '',
  );

  return lines.join('\n');
}

function main() {
  const tracked = listTrackedPine();
  const files = fs.readdirSync(PINE_DIR)
    .filter(file => file.endsWith('.pine'))
    .sort((a, b) => a.localeCompare(b));
  const inventory = files.map(file => analyzeFile(file, tracked));
  fs.mkdirSync(path.dirname(OUT_MD), { recursive: true });
  fs.mkdirSync(path.dirname(OUT_JSON), { recursive: true });
  fs.writeFileSync(OUT_MD, renderMarkdown(inventory), 'utf8');
  fs.writeFileSync(OUT_JSON, JSON.stringify({
    ok: true,
    generated_at: new Date().toISOString(),
    inventory,
  }, null, 2), 'utf8');
  console.log(`pine inventory files: ${inventory.length}`);
  console.log(`markdown: ${path.relative(ROOT, OUT_MD).replace(/\\/g, '/')}`);
  console.log(`json: ${path.relative(ROOT, OUT_JSON).replace(/\\/g, '/')}`);
}

main();
